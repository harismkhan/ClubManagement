class Player {
    constructor(id, firstName, lastName, birthDate, street, city, zip, height, weight, playerNumber, clubId, teamId)
    {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.birthDate = birthDate;
        this.street = street;
        this.city = city;
        this.zip = zip;
        this.height = height;
        this.weight = weight;
        this.playerNumber = playerNumber;
        this.clubId = clubId;
        this.teamId = teamId;
    }
}